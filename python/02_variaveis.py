# Variáveis são "caixinhas" que guardam valores
# O Python descobre automaticamente o tipo do dado

nome = "Ana"           # texto = string (str)
idade = 28              # número inteiro = int
salario = 4500.50       # número decimal = float
trabalhando = True      # verdadeiro/falso = boolean (bool)

# Mostrando os valores
print(nome)
print(idade)
print(salario)
print(trabalhando)

# Mostrando o TIPO de cada variável
print(type(nome))
print(type(idade))
print(type(salario))
print(type(trabalhando))